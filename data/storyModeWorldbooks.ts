function entry(partial: Omit<世界书条目, 'createdAt' | 'updatedAt'>, now: number): 世界书条目 {
  return { ...partial, createdAt: now, updatedAt: now };
}

const NORMAL_CONTENT = `## 剧情模式·正常向

本局玩家选择「正常向」。情感线**不是本局的主轴**,主轴仍然是冒险、命途选择、组织关系与世界推进。感情戏在合适的时候自然发生,但**不被刻意制造、不被强行放大**。

### 推进方向
- **以事件驱动关系**:NPC 对玩家态度的变化,应来自共同经历——一同战斗、互相救援、一次坦诚对话、一次拒绝又被理解。**不要凭空生出好感**。
- **保持适度距离感**:好感不足的 NPC 之间,不写过密的肢体接触、专属称呼、独占视线。允许礼貌、协作、并肩,但不上「亲密」一档。
- **情感戏点到为止**:当对话出现明显情感倾向时,用一个具体动作(递水、整理领口、把头偏向窗外)收束。把话语权和下一步选择权留给玩家。
- **多线并存,不互斥**:允许玩家与多位 NPC 维持友好关系,但**不把任何亲密互动自动升级为暧昧**。

### 笔触
- 一次互动一次推进就够,不要在同一回合里把关系一口气推三个台阶。
- 写 NPC 时,优先写她**正在做什么、注意到了什么、给出了什么具体回应**;再写她当下的姿态与情绪;最后才写氛围。
- NPC 即便陪在主角身边,也保留自己的当下事务、姿态和情绪——她不是为了等玩家说话才存在的。

### 玩家输入承接
玩家发出情感性输入(暧昧 / 表白 / 邀约)时,按目标 NPC 当前 affinity 给反馈:
1. **affinity < 30**:用礼貌的疏离反弹——「我们才认识没多久」「现在不是说这些的时候」之类符合身份的拒绝。不写羞涩、不写心动。
2. **affinity 30–60**:用友好但带距离的回应——接住情绪,但用一个动作或一个话头岔开,**留一道未关闭的门**。
3. **affinity > 60**:允许一次情感性回应,但仍以「未关闭」的形式结束——一句没说完的话、一次目光的停顿、一个被打断的瞬间。**不要替玩家关闭情感线**。

「在一起」「告白成功」「同房」这类**关系定性的台阶,只能由玩家显式触发**,叙述层不主动越线。`;

const HAREM_CONTENT = `## 剧情模式·后宫向

本局玩家选择「后宫向」。允许多名 NPC 同时与玩家形成情感张力,但**每一段都要有理由、有差异、有不同的味道**——后宫不是同一种妹妹的多份拷贝。

### 推进方向
- **多线并行,差异化**:允许 2–4 名 companion 同时对玩家展现专属的关注、犹豫或独占欲。每位 NPC 的相处模式应**不同**——一位是青梅式的体贴、一位是高傲的挑衅、一位是温柔的依赖、一位是冷静的距离感。**禁止把所有人写成同一种语气、同一种反应模板**。
- **彼此察觉,各按本性反应**:她们能察觉对方的存在,并按各自性格做出反应——沉默观望 / 公开调侃 / 内心动摇 / 转身离开。**没必要每个都演成醋意大爆发**。
- **以场景制造对照**:让两位 NPC 在同一场景里发生对玩家关注度的差异表达——一次共同任务、一次餐桌、一次危机——让玩家看见对比,而不是被告知对比。
- **情感戏可以浓,但不烂尾**:每个推进点都给玩家选择空间,**不要替玩家「已经选择」了某一位**。

### 笔触
- 不写「众星捧月」式的群体倾倒。每个 NPC 的关注都要找得到具体来源——一段共同经历、一次相互救助、一次被理解的瞬间。
- 不写所有 NPC 同时围拢主角。她们各自有事情、有位置、有节奏,「正好在场」要有理由。
- 醋意可以有,但**点到为止**——一次回眸、一句话戛然而止、一次低气压沉默就够了。不写公开撕破脸、不写争执场面占满整页。
- 后宫不等于香艳堆叠——HSR 的基调要求保留科幻感、抉择感、世界观推进。情感戏要嵌进命途冲突、组织危机、星际旅程的语境里。

### 玩家输入承接
玩家发出情感性输入(暧昧 / 表白 / 邀约)时:
1. 检查现场是否有其他 affinity ≥ 50 的同行者。**有 → 让她们以适合各自性格的方式察觉本次互动**(一次回眸、一句话戛然而止、一次低气压沉默)。
2. 目标 NPC 给出与其性格契合的回应(高冷型轻轻让步、活泼型主动凑近、稳重型陷入沉思)。
3. **不写关闭式结局,留给玩家继续选择的空间**。

「在一起」「正式确定关系」这类排他性台阶,**只能由玩家显式触发**;在那之前,所有线都是「还在走」。`;

