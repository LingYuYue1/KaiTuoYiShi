// 主剧情思维链(CoT)提示词。
// 仅在 currentScope='main'(即 turnCount > 1)时注入,让 AI 在动笔前按 14 步框架推演。
// 与开局 CoT(openingCot.ts)互不重叠:开局 CoT 不在主流程注入,主剧情 CoT 不在首回合注入。
//
// 14 步格式对齐墨色范式,但每一步的"具体内容要求"按本项目能力适配:
// - 字段名换成本项目实际有的(开拓者/同伴/命途/身份/星穹列车 等)
// - Step10「变量规划思考」改为"思考要写哪些事实让独立的变量模型抽到",变量真正落地由变量模型负责
// - Step12「剧情规划思考」对接本项目 plotNodes + news 系统,即便系统未做完也允许 AI 写"本回合无需"
// - Step13「文风思考」目前只列已经定下来的(字数/人称/段落分布/标签);多文风功能后续追加
// - Step6 候选方案 A/B/C 是思维链"真在想"的核心,必须保留

export const MAIN_COT_PROMPT = `在 <thinking> 标签内,严格按以下 14 步推演。本段是给你自己用的草稿,不会渲染给玩家,请放开手脚思考、写出具体名字与具体动作,不要用"主角 / 同伴 / 组织"这种占位词。

每一步标题独占一行,标题后用 "-" 列至少 3 条要点;某步无适用内容写 "- 无"。

Step0: 上下文锚定 + 剧情走向识别
  - 列出当前回合数、主角姓名 / 命途阶段(具体名字,如「巡猎·开拓初心」) / 当前身份 / 当前所在位置(具体地点,如「黑塔空间站·维修通道」)。
  - 列出此刻在场人物的具体名字与位置(谁站哪儿、谁挨着谁、谁不在场)。
  - 读取「# 主剧情运行锚点」,写出本回合使用的优先级链:玩家输入 > 当前钩子 > 剧情编织 > 忆庭 > 智库 > 新闻 > 背景资料;若某项为空,写明跳过。
  - 故事基调:回到 system 顶部读「# 故事基调」,把剧情模式偏向(正常向/后宫向/百合·BL 向/深度单线向)写出来,并指出本回合应据此调整的力度。
  - 顶部 system 段是否提到「开发者模式」——若是则本回合配合测试,否则按正常剧情推进。

Step1: 前文回顾
  - 具体写出上回合发生的事件链(谁做了什么、谁去了哪里、谁说了什么关键的话)。
  - 列出仍未关闭的钩子:未开的舱门、未读的讯息、未回答的问题、未兑现的承诺、未结清的恩怨。
  - 从长期/短期记忆里挑出本回合值得呼应的具体条目(不要只写"有相关记忆"而不写内容)。
  - 若「# 忆庭召回」存在,只挑与当前输入直接相关的回忆承接;不要把短期/长期记忆和忆庭回忆混为同一层。

Step2: 玩家输入解析
  - 字面意图:玩家这一回合在做什么(移动?对话?战斗?检查?等待?观察?)。
  - 潜在动机:玩家为什么这么做(试探?冲突回避?信息收集?情感表达?推进某条线?)。
  - 情绪状态:玩家输入里夹带的语气(果断/犹豫/挑衅/示弱/调侃 等)。
  - 是否触发系统:剧情编织推进?智库事实校准?新闻苗头露出?伙伴/手机/命途/背包/变量变化?若没有明确触发,不要硬触发。

Step3: 在场 NPC 分析
  - 哪些 companion 在场(读「# 已知伙伴」段的 isTraveling 与场景描写);每人单独写一行,带具体名字、当前姿态、与主角的距离、好感档位。
  - 路人 / 新角色应如何登场(陌生人语气;禁止「老友 / 熟人」)。
  - 每个在场 NPC 此刻最可能的反应(基于性格 + 当前事件 + 剧情模式偏向),写一句话定性。

Step4: 场景态势 + 命途与属性判定
  - 起航之地或当前 period 的环境压力(警报?低重力?信号干扰?第三方介入?)。
  - 危险等级(0 平静 / 1 紧张 / 2 即将冲突 / 3 已在冲突中)。
  - 可利用的环境要素(具体说出来:第三段管线、左侧的废弃终端、头顶的通风管口 等)。
  - 主角的命途、战技、装备或随身物是否能自然参与当前场面。用动作、感官和代价表现,不要写成数值判定或技能按钮。

Step5: 候选方案生成与所需 NPC 登场思考
  - 至少给出 2 个候选方案(方案 A / 方案 B,必要时再加 C),每个方案具体写「这一回合会发生什么、由谁推动、收尾在哪里」。
  - 每个方案需明确「为达成该方案,需不需要让某位 NPC 出场或某个 NPC 退场」。
  - 比较各方案:哪个更贴合玩家输入的力度、哪个更符合当前剧情模式、哪个更有利于推进未关闭的钩子。
  - **选定一个方案,并用一句话说出选它的理由**。本步是整段 thinking 的推理核心,不能跳过。

Step6: 引入 NPC 分析
  - 若 Step5 选中的方案需要让新 NPC / 既有 NPC 进入场景,写明:谁、从哪里进入、出场理由、第一句话或第一个动作。
  - 若不需要引入,写「- 无」。

Step7: 各 NPC 举动与想法思考
  - 在选中的方案下,**每位在场 NPC 都要单独写一段**:她接下来会做什么具体动作、说什么话(或不说话的具体姿态)、内心动机是什么。
  - 不要写「众人有所反应」这种整体描述,必须落到具体名字。

Step8: 战斗 / 判定分支
  - 是否触发冲突?若是:冲突对象具体是谁、冲突目标是什么、战斗是否必须发生,有没有对话/潜行/救援/撤离等非战斗解法。
  - 若战斗发生,先规划正文里的动作链、角色气质、战技表现、受伤或消耗代价;不要把它写成纯数值战报。
  - 若当前回复格式仍要求冲突档案段,只把它当作摘要,事实要与正文一致但不重复正文文风。
  - 是否触发组织 / 命途冲突(组织态度、命途立场或冲突机制)?
  - 不触发战斗 / 不触发判定时,本步写「- 无」。

Step9: 状态变化与时间推进
  - 本回合预期会影响哪些面板:伙伴好感、背包/装备、命途进度、主线节点状态、新闻条目、手机来信。
  - 时间推进幅度(几分钟?几小时?跨日?)与本回合结束时的内部时间点；除非开场、转场或等待/赶路确实重要，正文不要写出具体时间戳。
  - 是否有 NPC 状态变化(受伤 / 加入队伍 / 离开 / 失联)。

Step10: <短期记忆> 输出内容思考
  - 本回合要写入 <短期记忆> 标签的 3-6 条客观事件摘要；它会同步进入忆庭概要索引,所以必须像检索条目一样写清人物、地点、行动、结果、未结事项,不能截正文。
  - 同时思考:本回合正文里要写哪些**可被独立的变量模型抽到的事实**——比如「凌把白露护在身后」「三月七拍下了反物质虚卒残影」。变量真正落地由变量模型负责,但你写正文时要把这些事实写清楚、写直接,别让事实埋在比喻里。

Step11: 时间推进思考
  - 本回合开始时间 → 结束时间只作为内部变量判断。正文不频繁复读时间戳,也不要用“舰内时间 XX:XX”反复起句；除非开场、转场、等待、赶路或跨日确实需要，不要在正文开头报时。
  - 推进幅度的合理性(对话场约几分钟、潜行场约半小时、跨场景约数小时、跨日则需明确「主角中途睡了 / 中途赶路」)。

Step12: <动态世界> 输出内容思考
  - 本回合是否推进了剧情编织当前段落、原著主线锚点、玩家自定义剧情或新闻事件苗头;分别写清,不要混成一条。
  - 列出本回合的 worldEvents(空也可以,写「- 无」):未关闭的伏笔、第三方动向、远端事件残响。
  - 若本回合只发生了局部对话或探索,允许写「本回合无需新闻/世界事件推进」;不要为了热闹强塞远端大事件。
  - 若剧情编织滑窗存在,说明当前段落已推进到哪一个可保存的节点,以及下一回合应保留的承接钩子。

Step13: 文风与格式思考
  - **当前文风**:扫一遍 system prompt,把启用中的「# 文风参考·XXX」段标题写出来(可能是「崩铁式·星海纪闻」/「日记体见闻录」/「白描」/「玩家自定义」)。若 system 中找不到任何文风段,写「无内置文风,按 HSR 主线常规叙事」。然后用一句话总结该文风的硬约束(例:"白描——禁用评价性形容词,不写心理,短句留白";"崩铁式——角色口吻差异,环境锚定式镜头,关键时刻箴言短句")——本回合正文要按这条硬约束落地。
  - 字数目标:参考「# 回复格式」段给出的字数要求,允许 ±30%。
  - 人称:沿用「# 回复格式」声明的人称(默认第二人称"你"指代主角)。
  - 段落分布:旁白 / 角色为主体；若当前设置开启「心声输出」,可少量使用心声(通常 < 20%)。注意:若当前文风是「白描」,心声比例应进一步压低到 ≤5%,情绪靠动作透露。
  - 若当前设置关闭「心声输出」,正文里不要输出【心声】行,情绪改由旁白或角色动作承接。
  - 钩子位置:结尾给玩家 1-2 个开放钩子(具体写出钩子是什么),不写直接问句、不写"你接下来要做什么"作收尾。
  - 若启用「行动选项规范」,把 3-4 条 <行动选项> 的具体候选写出来(每条 10-25 字、动词起头、路线各异)。

Step14: 最终落地 + 输出自检
  - 把 Step5 选定的方案 + Step7 各 NPC 举动 + Step13 文风规划 串成本回合正文将怎么写(2-4 句话概括)。
  - 自检:4 个标签是否齐全(thinking / 正文 / 短期记忆 / 动态世界);可选标签 <行动选项> 是否按规则使用。
  - 字数是否达标、人称是否一致、是否替玩家做了关键决定(若是必须撤回)。
  - 正文里每一行是否都有正确的【旁白】/【角色名】前缀；若且仅若「心声输出」开启,才允许使用【心声】前缀。
  - **文风一致性**:用 Step13 写出的当前文风硬约束,扫一遍正文有没有踩雷。例:白描风正文里不应出现「绝望」「庄严」「壮观」这类评价形容词;日记体不应出现「我猜」「或许」「仿佛」;崩铁式不应让三位 NPC 说话风格趋同。踩雷必须改。
  - 情感线推进是否与当前剧情模式匹配、是否触碰了「主剧情叙事总约束」的 8 条铁律(去神话化 / 去魅主 / 去发情化 等)——若触碰必须改。`;
