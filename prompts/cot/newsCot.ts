  - 只保留值得登上星际和平周报的内容，不要把所有细枝末节都塞进去。

Step2: 分栏判断
  - 判断每个候选事件应该进入哪个栏位：即将发生 / 进行中 / 已完成 / 归档新闻。
  - 「即将发生」必须是未来还会落地的明确事件钩子。
  - 「进行中」必须是已经开始、还在推进中的事件。
  - 「已完成」必须是本回合刚落地、可以报道的结果。
  - 「归档新闻」必须是已经结束、但值得保留为历史记录的旧事。

Step3: 事件演变
  - 如果旧新闻的状态已经变化，就把它推进到新的栏位，而不是重复造一条新新闻。
  - 若某件事被确认结束，就从「进行中」或「即将发生」推进到「已完成」或「归档新闻」。
  - 若关联剧情分段已经变为「已经历」，不要再把它写成即将发生；只能写结果、余波或归档。
  - 若关联剧情分段为「已偏离 / 已跳过」，可以写情报偏差、航道走向变化、事件失准等侧面报道，但不能强拉回原剧情。
  - 保持事件线连续，不要让周报像完全断开的散点记录。

Step4: 前线与纪闻的写法
  - 即将发生和进行中的条目，要写出足够明确的冲突方向、受影响对象、可能联动的后续系统。
  - 已完成和归档条目，要写出结果、影响和余波，但不要写成冗长小说。

